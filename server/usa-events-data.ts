// Helper functions
export function getEventBySlug(slug: string): USAEvent | undefined {
  return usaEvents.find(event => event.slug === slug);
}

export function getEventsByCity(city: string): USAEvent[] {
  return usaEvents.filter(event =>
    event.city.toLowerCase().includes(city.toLowerCase())
  );
}

export function getEventsByCategory(category: string): USAEvent[] {
  return usaEvents.filter(event => event.category === category);
}

export function getEventsByMonth(month: string): USAEvent[] {
  return usaEvents.filter(event =>
    event.month.toLowerCase().includes(month.toLowerCase())
  );
}

export function getFeaturedEvents(): USAEvent[] {
  return usaEvents.filter(event => event.featured);
}

export function getTier1Events(): USAEvent[] {
  return usaEvents.filter(event => event.tier === 1);
}

export function getTier2Events(): USAEvent[] {
  return usaEvents.filter(event => event.tier === 2);
}

export function getAllCategories(): string[] {
  return Array.from(new Set(usaEvents.map(event => event.category)));
}

export function getAllCities(): string[] {
  return Array.from(new Set(usaEvents.map(event => event.city))).sort();
}

export function getAllMonths(): string[] {
  const months = ['January', 'February', 'March', 'April', 'May', 'June',
                  'July', 'August', 'September', 'October', 'November', 'December'];
  return months;
}
